#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;
	int		i;

	i = 0;
	s = s1;
	while (*s != '\0')
	{
		s++;
	}
	while (*s2 != '\0' && n > 0)
	{
		*s = s2[i];
		s++;
		i++;
		n--;
	}
	*s = '\0';
	return (s1);
}
