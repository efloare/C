#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (s1 && s2)
	{
		ft_memset(s1, '\0', n);
		i = 0;
		while (n-- > 0 && s2[i])
		{
			s1[i] = s2[i];
			i++;
		}
	}
	return (s1);
}
