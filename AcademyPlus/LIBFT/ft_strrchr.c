#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ch;

	ch = NULL;
	if (s)
	{
		while (*s)
		{
			if (*s == c)
				ch = (char *)s;
			s++;
		}
		if (*s == c)
			ch = (char *)s;
	}
	return (ch);
}
