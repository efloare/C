#include <unistd.h>

int main( int ac, char **av)
{
	int i;
	int k;
	int n;
	int s;

	s= 0;
	i = 0;
	n = 0;
	k = 0;
	if(ac !=3)
	{
		write (1, "\n", 1);
		return (0);
	}
	while(av[1][s] != '\0')
		s++;
	while(av[1][i] != '\0' && av[2][k] != '\0')
	{
		while((av[1][i] == av[2][k]) && (av[1][i] != '\0' && av[2][k] != '\0'))
		{
			k++;
			i++;
			n++;
		}
		k++;
	}
	if (s == n)
		write(1, "1",1);
	else
		write(1, "0",1);
	write(1, "\n", 1);
	return (0);
}
