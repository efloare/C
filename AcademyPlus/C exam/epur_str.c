#include <unistd.h>

int main( int ac, char **av)
{
	int i;
	int k;

	i = 0;
	k = 0;
	if( ac == 2)
	{
		while(av[1][i] == ' ' || av[1][i] == '\t')
			i++;
		while(av[1][i] != '\0')
		{
			if(av[1][i] == ' ' || av[1][i] == '\t')
				i++;
			while(av[1][i] != ' ' && av[1][i] != '\t' && av[1][i] != '\0')
			{
				write(1, &av[1][i], 1);
				i++;
			}
			if(av[1][i + 1] != ' ' && av[1][i +1] != '\t' && av[1][i] != '\0' && av[1][i +1] != '\0')
				write(1, " ", 1);
		}

	}
	write(1, "\n", 1);
	return (0);
}
