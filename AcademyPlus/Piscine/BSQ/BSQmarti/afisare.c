#include "head.h"

void		afisare(char **str, t_tot tot)
{
	int		i;
	int		j;

	i = 1;
	j = 0;
	while (i < tot.y)
	{
		while (j < (tot.x + 1))
		{
			ft_putchar(str[i][j++]);
		}
		j = 0;
		i++;
	}
}
