#include "ft_do_op.h"

long	sum(long a, long b)
{
	return (a + b);
}

long	subtract(long a, long b)
{
	return (a - b);
}

long	mul(long a, long b)
{
	return (a * b);
}

long	div(long a, long b)
{
	return (a / b);
}

long	mod(long a, long b)
{
	return (a % b);
}
